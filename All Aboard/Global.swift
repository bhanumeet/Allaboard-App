        AudioServicesPlaySystemSound(kSystemSoundID_Vibrate)
    }
    
    /// Stops the long vibrations
    static func stopLongVibrate() {
        AudioServicesDisposeSystemSoundID(kSystemSoundID_Vibrate) // heavy tack
    }
}
